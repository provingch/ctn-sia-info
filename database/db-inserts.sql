SELECT r.id AS registro_id, t.id AS tarea_id,
       ( (r.id * 37 + t.id * 13) % (t.total - 29) ) + 30 AS puntos
FROM registro r
JOIN tarea t ON t.planilla_id = r.planilla_id
WHERE r.planilla_id IN (5,11,13,14,19,25,27,28)
ORDER BY r.id, t.id
